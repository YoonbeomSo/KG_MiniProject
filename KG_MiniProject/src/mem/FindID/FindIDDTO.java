package mem.FindID;

public class FindIDDTO {
	private String Name;
	private int Birth;
	private int Mobile;
	
	
	public String getName() {
		return Name;
	}
	public void setName(String name) {
		Name = name;
	}
	public int getBirth() {
		return Birth;
	}
	public void setBirth(int birth) {
		Birth = birth;
	}
	public int getMobile() {
		return Mobile;
	}
	public void setMobile(int mobile) {
		Mobile = mobile;
	}
	
	
}
